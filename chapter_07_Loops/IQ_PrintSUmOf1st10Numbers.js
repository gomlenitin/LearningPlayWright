console.log("Executing for loop")

let sum = 0;
for (let n = 1; n <= 10; n++) {   //(Initi; condition; incrememnt)
    sum = sum + n;
    //console.log("Sums of 1st 10 numbers =" + sum)
}
console.log("Sums of 1st 10 numbers =" + sum)



